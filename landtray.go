func createLockFile(filename string) (*os.File, error) {
	file, err := os.OpenFile(filename, os.O_WRONLY, 0666)
	if err != nil {
		return nil, err
	}
	err = syscall.Flock(int(file.Fd()), syscall.LOCK_EX|syscall.LOCK_NB)
	if err != nil {
		return nil, err
	}
	return file, nil
}

func status(items map[string]*systray.MenuItem) {
	// Ous status is ok on default
	ok := true

	// Get the handle of the file
	file, err := os.Open("/tmp/results.txt")
	if err != nil {
		fmt.Println(err)
		return
	}
	// Releate the handle when the function exists
	defer file.Close()

	// Read the file line by line
	scanner := bufio.NewScanner(file)
	for scanner.Scan() {
		// Split every line into a slice by the ';' char
		s := strings.Split(scanner.Text(), ";")
		fmt.Println(s)
		// We must have 4 splits to be ok
		if len(s) != 4 {
			continue
		}
		// Give some format to the info
		txt := fmt.Sprintf("%s (%s+%ss)", s[0], s[3], s[2])
		// Create the menu item if it does not exist
		if _, found := items[s[0]]; !found {
			items[s[0]] = systray.AddMenuItem(txt, txt)
		}
		// Set the title
		items[s[0]].SetTitle(txt)
		// Change the state depending on the state
		if s[1] == "OK" {
			items[s[0]].Enable()
		} else {
			items[s[0]].Disable()
			// We are not ok!
			ok = false
		}
	}

	// Fire an error if the file reading failed
	if err := scanner.Err(); err != nil {
		fmt.Printf("Error '%v' reading file.\n", err)
	}

	// Choose an icon depending on the execution state
	if ok {
		systray.SetTemplateIcon(okIcon, okIcon)
	} else {
		systray.SetTemplateIcon(nokIcon, nokIcon)
	}
	// Set the title/tooltip
	systray.SetTitle("Landscape")
	systray.SetTooltip("Landscape")
}

func onExit() {
	fmt.Println("Exiting...")
}

func onReady() {
	fmt.Println("Ready")

	// Map of the systray items to keep on runtime
	var items = map[string]*systray.MenuItem{}
	// First call to populate the systray
	status(items)
	// Timer object to tick our status function
	t := time.NewTicker(time.Duration(300 * time.Second))
	// This goroutine will be listening to the ticker
	go func() {
		for now := range t.C {
			fmt.Printf("Tick at %v.\n", now)
			status(items)
		}
	}()
}

func main() {
	// Only one instance at a time
	s := single.New("landtray")
	if err := s.CheckLock(); err != nil && err == single.ErrAlreadyRunning {
		fmt.Println("another instance of the app is already running, exiting")
		os.Exit(1)
	} else if err != nil {
		// Another error occurred, might be worth handling it as well
		fmt.Println("failed to acquire exclusive app lock: %v", err)
		os.Exit(1)
	}
	defer s.TryUnlock()

	// Create the systray
	systray.Run(onReady, onExit)
}
